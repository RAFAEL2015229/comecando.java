public class OperadoresAritmeticos {
     public static void main (String[] args){


        int a = 10;
        int b = 20;
        
        int soma = a + b;

        System.out.println("Soma e: " + soma);
        
        int subtracao = a - b;
        
        System.out.println("subtraçao e: " + subtracao);
        
        int multiplicacao = a * b;
        
        System.out.println("multiplicação e: " + multiplicacao);
        
        int divisao = b / a;
        
        System.out.println("divisão e: " + divisao);
        
        int modulo = a % b;
        
        System.out.println("modulo e: " + modulo);
        
        int incremento = 10;
        int decremento = 10;
        
        incremento++; 
        
        System.out.println("valor apos adicionado o incremento : " + incremento);
        
        decremento--; 
        
        System.out.println("valor apos adicionado o decremento: " + decremento);

        
}
}